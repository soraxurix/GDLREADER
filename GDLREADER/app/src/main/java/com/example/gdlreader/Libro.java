package com.example.gdlreader;

public class Libro {

    String Titulo, Autor, Genero, Url;
    int NoLibro;

    public String getTitulo() {
        return Titulo;
    }

    public String getAutor() {
        return Autor;
    }

    public String getGenero() {
        return Genero;
    }

    public String getUrl() {
        return Url;
    }

    public int getNoLibro() {
        return NoLibro;
    }
}
